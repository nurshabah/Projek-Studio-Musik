    private void idDaftarTxtFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_idDaftarTxtFocusLost
        if(idDaftarTxt.getText()==""){
            namaDaftarTxt.setText("");
            no_telpDaftarTxt.setText("");
            alamatDaftarTxt.setText("");
            
        }
        else{
            try{
                koneksi();
                String sql="select nama_member,no_telepon,alamat from member where id_member='"+idDaftarTxt.getText()+"'";
                Statement stat = conn.createStatement();
                ResultSet rs=stat.executeQuery(sql);
                while(rs.next()){
                    namaDaftarTxt.setText(rs.getString("nama_member"));
                    no_telpDaftarTxt.setText(rs.getString("no_telepon"));
                    alamatDaftarTxt.setText(rs.getString("alamat"));
                    HapusDaftar.setEnabled(true);
                    EditDaftar.setEnabled(true);
                    SimpanDaftar.setEnabled(false);

                }
            }catch(Exception e){
                e.printStackTrace();

            }
        }
    }//GEN-LAST:event_idDaftarTxtFocusLost

