public void AutoNumberSewa() {
        try {
            String sql = "SELECT MAX(right(no_sewa,2)) AS no_sewa FROM sewa";
            koneksi();
            ResultSet rs=cn.executeQuery(sql);
                while (rs.next()){
                    if(rs.first()== false){
                      no_sewaTxt.setText("NS0001");
                    }
                    else{
                        rs.last();
                        int auto_id = rs.getInt(1)+1;
                        String no = String.valueOf(auto_id);
                        int noLong = no.length();
                            for(int a = 0; a<4-noLong; a++){
                                no = "0" + no;
                            }
                            no_sewaTxt.setText("NS"+ no);
                    }
                }
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Error: \n" + e.toString(), "Kesalahan", JOptionPane.WARNING_MESSAGE);
        }
    }
